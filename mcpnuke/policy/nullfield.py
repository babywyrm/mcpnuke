"""Serialize policy rules to nullfield NullfieldPolicy YAML."""

from __future__ import annotations

from typing import Any

from mcpnuke.policy.rules import PolicyRule


def serialize_policy(
    rules: list[PolicyRule],
    name: str = "mcpnuke-recommended",
    namespace: str = "",
) -> str:
    """Convert PolicyRule list to nullfield-compatible YAML string."""
    policy: dict[str, Any] = {
        "apiVersion": "nullfield.io/v1alpha1",
        "kind": "NullfieldPolicy",
        "metadata": {"name": name},
        "spec": {
            "selector": {"matchLabels": {}},
            "rules": [],
        },
    }

    if namespace:
        policy["metadata"]["namespace"] = namespace

    for rule in rules:
        entry: dict[str, Any] = {
            "action": rule.action,
            "mcpMethod": "tools/call",
            "toolNames": rule.tool_names,
            "reason": rule.reason,
        }

        if rule.hold:
            entry["hold"] = rule.hold
        if rule.scope:
            entry["scope"] = rule.scope
        if rule.budget:
            entry["budget"] = rule.budget

        policy["spec"]["rules"].append(entry)

    return _manual_yaml(policy)


def _manual_yaml(policy: dict) -> str:
    """Fallback YAML serializer when PyYAML is not available."""
    import json
    lines = ["# Generated by mcpnuke --generate-policy"]
    lines.append(f"apiVersion: {policy['apiVersion']}")
    lines.append(f"kind: {policy['kind']}")
    lines.append("metadata:")
    lines.append(f"  name: {policy['metadata']['name']}")
    if "namespace" in policy["metadata"]:
        lines.append(f"  namespace: {policy['metadata']['namespace']}")
    lines.append("spec:")
    lines.append("  selector:")
    lines.append("    matchLabels: {}")
    lines.append("  rules:")

    for rule in policy["spec"]["rules"]:
        lines.append(f"    - action: {rule['action']}")
        lines.append(f"      mcpMethod: {rule['mcpMethod']}")
        tools = json.dumps(rule["toolNames"])
        lines.append(f"      toolNames: {tools}")
        lines.append(f"      reason: \"{rule['reason']}\"")
        for key in ("hold", "scope", "budget"):
            if key in rule:
                lines.append(f"      {key}:")
                for k, v in rule[key].items():
                    if isinstance(v, dict):
                        lines.append(f"        {k}:")
                        for kk, vv in v.items():
                            lines.append(f"          {kk}: {json.dumps(vv)}")
                    else:
                        lines.append(f"        {k}: {json.dumps(v)}")

    return "\n".join(lines) + "\n"
